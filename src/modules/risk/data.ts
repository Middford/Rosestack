// ============================================================
// Score History (simulated)
// ============================================================

export interface ScoreHistoryEntry {
  date: string;
  probability: number;
  impact: number;
  score: number;
}

export function generateScoreHistory(item: RiskItem | OpportunityItem): ScoreHistoryEntry[] {
  const entries: ScoreHistoryEntry[] = [];
  const months = ['2025-10', '2025-11', '2025-12', '2026-01', '2026-02', '2026-03'];

  for (let i = 0; i < months.length; i++) {
    const jitter = Math.sin(i * 1.5 + item.id.charCodeAt(4)) * 0.5;
    const prob = Math.max(1, Math.min(5, Math.round(item.probability + jitter)));
    const imp = item.impact;
    entries.push({
      date: months[i],
      probability: prob,
      impact: imp,
      score: prob * imp,
    });
  }

  return entries;
}

