export interface TimelineItem {
  id: string;
  name: string;
  type: 'risk' | 'opportunity';
  category: string;
  startMonth: number; // months from now
  endMonth: number;
  probability: number;
  score: number;
}

export function generateTimelineData(): TimelineItem[] {
  const items: TimelineItem[] = [];

  SEEDED_RISKS.forEach((r) => {
    const hash = r.id.charCodeAt(4) + r.id.charCodeAt(5);
    const start = (hash % 18);
    const duration = 6 + (hash % 24);
    items.push({
      id: r.id,
      name: r.name,
      type: 'risk',
      category: r.category,
      startMonth: start,
      endMonth: start + duration,
      probability: r.probability,
      score: r.score,
    });
  });

  SEEDED_OPPORTUNITIES.forEach((o) => {
    const hash = o.id.charCodeAt(4) + o.id.charCodeAt(5);
    const start = (hash % 12);
    const duration = 6 + (hash % 30);
    items.push({
      id: o.id,
      name: o.name,
      type: 'opportunity',
      category: o.category,
      startMonth: start,
      endMonth: start + duration,
      probability: o.probability,
      score: o.score,
    });
  });

  return items;
}
